char	*ft_strcpy(char *str1, const char *str2)
{
	char	*s1;

	s1 = str1;
	while (*str2 != '\0')
	{
		*s1 = *str2;
		s1++;
		str2++;
	}
	*s1 = '\0';
	return (str1);
}
