#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	char	*s1;

	s1 = str1;
	while (*s1 != '\0')
		s1++;
	while (*str2 != '\0')
	{
		*s1 = *str2;
		s1++;
		str2++;
	}
	*s1 = '\0';
	return (str1);
}
